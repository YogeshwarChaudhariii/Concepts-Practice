#include<iostream>
#include<array>
using namespace std;

int main()
{
    // It will take 0 automatically for other index. ie. {11, 21, 51, 0, 0}
    // Stores 0 internally
    array<int, 5> Arr = {11, 21, 51};

    for (int i = 0; i < Arr[i]; i++)
    {
        cout<<Arr[i]<<" ";
    }
    

    return 0;
}
